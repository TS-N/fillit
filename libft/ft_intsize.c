#include "includes/libft.h"

size_t		ft_intsize(int n)
{
	size_t	len;

	len = 0;
	if (n == 0)
		return (1);
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n *= -1;
		++len;
	}
	while (n >= 1)
	{
		n /= 10;
		++len;
	}
	return (len);
}
